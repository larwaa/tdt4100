package of5;

public class Person {
	
	private int workYears, delayedCount;

	public int getWorkYears() {
		return workYears;
	}

	public void setWorkYears(int workYears) {
		this.workYears = workYears;
	}

	public int getDelayedCount() {
		return delayedCount;
	}

	public void incrementDelayed() {
		delayedCount ++;
	}
	
	
	
	
	
}
